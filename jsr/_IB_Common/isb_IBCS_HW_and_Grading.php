<h2>HW Assignments</h2>
<p><strong>Copying &amp; pasting of answers</strong></p>
<p>Copying and pasting answers from the website notes pages to the homework assignments accomplishes very little, that finding the answer did not already accomplish. The only extra addition to your life and your brain is reinforcement of the muscle memory for Command-C and Command-V.</p>
<p>But, when you find an answer, do your best to comprehend it, and then re-work it into your own words, different synapses of your brain do indeed fire, you better appreciate and store that information, and you are much more likely to be able to retrieve and/or apply it.</p>
<p>Blatant copy &amp; pasting on a 1 - 7 graded formative assignment will result in one or two points deducted from the full assessed value of the assignment.</p>
<p>&nbsp;</p>
<p><strong>A good homework strategy</strong></p>
<p>The “Look away” strategy for answering HW questions:<br />
  (also known as the “Frank Sinatra” way- i.e. the “I did it my way” way)</p>
<p>The idea is to read through the notes, read through the question, and then, without going back and forth to the notes and simply re-wording, actually think (after having read and understood the relative part of the notes) how you would explain it in your own words. Then look back to make sure you were on target.</p>
<p>Summary: How to put in own words:<br />
  1. Read<br />
  2. Understand<br />
  3. Go to question and write your answer without looking at the original.<br />
</p>
<p>&nbsp;</p>
<p><strong>HW &amp; MSH</strong></p>
<p>Formative homework assignments are a big part of the course; it is a big part of your learning. Meantime, MSH is part of the school day, and part of this school’s system; I work against the system if I do not use it as it is intended. If you get behind in homework, which, again, is part of the course, it is MSH that will come to your rescue! (In around 3 missed will be the trip-wire point.)<br />
</p>
<p>&nbsp;</p>
<p><strong>HW late deductions<br />
</strong></p>
<p>If a homework assignment is not submitted by the time I mark it, in PowerSchool it will be recorded Missing, with the Missing orange square flag.</p>
<p>Then, when you make it up, e-mail me (don’t just tell me, please) to let me know, and I’ll grade it. I’lll then take away the Missing orange square flag, and add a Late red square flag. </p>
<p>Plus, I'll deduct one mark for any homework submitted late; so a 7 turns into a 6, a 5 turns into a 4 etc. </p>
<p>Refer to the Formative effects on grades section below to see that yes, even this could possibly influence an overall grade determination if tie-breaking is needed.</p>
<p>This does not apply for when you miss due to illness, but it does in all other cases. </p>
<p>And a reminder here that the policy of MSH after three incomplete assignments are accumulated, remains.</p>
<p>&nbsp;</p>
<h2><strong>Grades &amp; Grading<br />
</strong></h2>
<p><strong>Which grade scale to use, ✓, ✓-, ✓+ or 1 - 7?</strong></p>
<p>If it’s just being graded based on completion, then ✓, ✓-, ✓+. <br />
  Otherwise, generally, if there is a spread/range, then 1-7<br />
  But if it’s pretty straight-forward and everybody should be able to do it fairly well, then ✓, ✓-, ✓+.</p>
<p>Though I’ll usually be able to tell you which scale (1 - 7 or  ✓, ✓-, ✓+) will be used, I'll reserve the right to change the choice at the time of grading. </p>
<p>Here’s an example of such a case from last year. For an IA assignment, from Criterion A, references were underlined again and again as being The Most Important Thing, and half of the students still had none when the work was submitted. That gives students a 2 out of 6 on the IB IA rubric. And that's not a ✓-!  So the 1 - 7 scale ended up having to be used, where I would have preferred to use the ✓, ✓-, ✓+ scale.<br />
</p>
<p>&nbsp;</p>
<p>1 - 7 &amp; ✓, ✓-, ✓+ for formative assignments</p>
<p>✓, ✓-, ✓+ formative mark given for the submission &amp; completion: </p>
<p>- All questions done or attempted, and it submitted on time is a ✓+<br />
  - All questions done or attempted, and it submitted late, but before the test is a ✓<br />
  - All questions done or attempted, and it submitted after the test is a ✓-<br />
  - Not submitted, Missing orange square flag.<br />
</p>
<p><br />
  ✓, ✓-, ✓+ graded formative assignment:</p>
<p>(where I go over everything, and offer comments and suggestions:<br />
  - ✓ is the default, “you did what you were supposed to do” mark, from where you <br />
  - ✓+, “super fantastic”, you went above and beyond<br />
  - ✓- “oh well, good attempt, but not quite there :-( .<br />
  Missing orange square flag - not submitted <br />
</p>
<p><br />
  1 - 7 graded formative assessment (importantly, not summative):</p>
<p>7 often seems an appropriate default from which you could only be deducted a certain amount.<br />
</p>
<p>&nbsp;</p>
<p><strong>Achievement level, not grade boundary.</strong></p>
<p>With 1 - 7, it’s how your work and your ability compare to the narrative description of that achievement level. So it’s a wholistic assertion by the teacher, not whether you have reached a certain calculated border number or not.</p>
<p>Note the difference between these two scenarios on a 1 -7 graded homework assignment:</p>
<p>Ummm…..Ok, ✓<br />
  Ummm…..Ok, ✓<br />
  Ummm…..Ok, ✓<br />
  Ummm…..Ok, ✓<br />
  Nope -1</p>
<p>and </p>
<p>Ka bang! ✓<br />
  Ka bang! ✓<br />
  Ka bang! ✓<br />
  Ka bang! ✓<br />
  Nope -1</p>
<p>The second scenario conveys much better understanding, even though the grade may end up the same. All of this is taken into consideration when a wholistic achievement level is considered.</p>
<p>The same goes for testing, and test answers.</p>
<p>&nbsp;</p>
<p><strong>Affect of formative grades on overall &amp; predicted grades</strong></p>
<p><strong>ISB grade:</strong><br />
  Poorly done, or incomplete, or missing formative assignments - particularly regular homework assignments - CAN, and REGULARLY DO affect the ISB achievement level attained. Formative assessments do not have as much affect as summative assessments. But keep in mind it is an effect, not a calculation. </p>
<p>It is not a mathematical average of summative assessments that we use here. Rather, final grades are judged in terms of achievement levels. And though summative assignments most inform that achievement level judgement, so too does everything in the body of work that the teacher has at their disposal. And formative assignments are, for sure, part of that body of work, even though their primary purpose is to continually get feedback about learning progress.</p>
<p><strong>IB predicted grade:</strong><br />
  Ultimately, IB final grades are indeed the result of mathematical calculations, and grading boundaries. But predicted grades are predicted grades. And achievement levels do, for sure, help the prediction. This is particularly true of IBCS, because a big part of the final grade is a result of success (or lack there-of) on the IA, which for HL is 20% and SL, a full 30% of the course. And, being a huge project, the IA is more about commitment, being able to be organized, and keeping up with work, than it is about anything else. And the biggest predictor of this is how a student is able to be organized and keep up with the regular homework assignments. (By the way, another great predictor is the EE, if the student chooses to do it in CS.)</p>
<p>You’ll note that poor or incomplete homework completion will most often affect an ISB or predicted grade, when the student is on the border between two achievement levels, or predicted scores.<br />
</p>
<p>&nbsp;</p>
<p><strong>Miscellaneous Grading Points</strong></p>
<p>A 5/6 or a 6/7 etc. marked on a test gets bumped up to the higher of the two on Powerschool since it takes only whole numbers.</p>
<p>&nbsp;</p>
<p><strong>Grades &amp; IBCS</strong></p>
<p>First of all, consider that if a student is getting 7s across the board, it may mean any number of things, but there’s one thing for sure that it means; it means they’re good at…… wait for it…… school. Sevens across the board means that student is good at School. And, yes, most often that student will likely be able to transfer this success to the wider world.</p>
<p>But, note that a 4 or 5 in your chosen subject does not mean you will not succeed in that, outside of a school context. What is more important for your future success is that you like the subject, and want to grow and develop in it.</p>
<p>And though it may be different for other subjects, with IBCS, to university admissions officers, an HL or SL in IBCS is not as important as the “CS” itself. What sets you apart is simply the fact that you have taken a real, academic CS course. Very few schools around the world offer IB/AP CS. The total number of students in the entire world who took IBCS last year was 4500. </p>
<p>Having taken IBCS, if you apply to a post secondary CS program, it tells the admissions officers that A. you know what CS is, and B. knowing what it is, it is an informed decision on your part to want to continue.<br />
</p>
<p>&nbsp;</p>
<p><strong>IBCS realistic grade expectations</strong></p>
<p>More than just about any other IB course, IBCS is consistently among the lowest average grades. This is just a fact. The average grade for HL IBCS, year to year, is always just a bit above 4, and for SL IBCS, it’s almost always below 4. With the fact that this is 4 out of a scale of 7, it ends up being around 32% HL and 42% SL who get 1 - 3, i.e. who do not pass the course.</p>
<p>Combined, the ISB SL and HL IBCS class of 2020 averaged around 25% above the world average. This is both significant, and good, and about what I would expect from a very good school such as ours. But please note that of the 17 students, there were no 7s. Sevens, in particular are rare anywhere - the account for 4.7% of the world scores in IBCS over the past five year0. These sevens are generally seen from schools with fully developed IT/CS programs, K - 12 (we’re getting there), by students there who have been coding for many years before reaching IBCS.</p>
<p>So, putting it bluntly, if it’s 7s you’re after, for whatever reason, then you’re in the wrong place. </p>
<p>And, in particular, if you have chosen to take the full IB diploma, and you need it for your post secondary plans, you need to be aware of the potential for lower than average average marks for IBCS. This is true both if you think you will have trouble getting the 24 points, and even more-so if with the SL/HL decision, if you think you will have trouble getting the 12 HL points that are required.<br />
  0<br />
  But if these things do not apply to you, or if you are in a healthier/better grade situation, for any other reason - like having graduated from an FSD (Freedom From Sevens Dependency) program - you too can expect to score relatively well to others around the world taking this course.<br />
</p>
<p>&nbsp;</p>
<p><strong>Grades &amp; the EE</strong></p>
<p>This is mainly for those doing their EE in CS, but there’s an important point, which applies to all, so read on. </p>
<p>The one thing that can result in a predicted grade going down in Year 2 is that the student cannot manage the big project that is the IA. It happens to several students every year, and in general, it’s hard for me to know who it will be. But if students are doing their EE in CS, that can help my prediction a lot. Those who cannot manage the big project which is the EE, I cannot give the benefit of the doubt for their IA’s influence on their grade. Failure, in other words, with the EE through Year 1, necessarily means that I have to consider likely failure with the IA in the predicted grade I issue to those students.<br />
</p>
<p>&nbsp;</p>
<p><strong>Predicted Grades and their importance</strong></p>
<p>Predicted grades, as they were originally intended, were mainly to help IB graders and moderators identify possible grading errors, by knowing what the teacher who knows the student best anticipated. Then, there’s their use by universities for helping to judge ability, and for making conditional offers (UK and some other mainly European countries - not North America.)</p>
<p>But just as importantly they are to help us help you get your diploma. And at a school like ours (and most international schools like us), it’s the 12 HL points that is the problem, since we don’t reach the HL hours required. So predicted scores by early Year 2 help us identify potential problems with SL/HL choices, and correct them, if possible, before it’s too late.</p>
<p>So it’s really important that we teachers give as accurate a prediction as we can. <br />
</p>
