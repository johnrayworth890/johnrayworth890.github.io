<h2>Miscellaneous Points about IBCS @ ISB with me, John Rayworth</h2>
<strong>Seating</strong>
<p>Feel free to sit anywhere, and in fact, make a point to mix it up, so that you work with other people - or I can mix it up for you occasionally.</p>
<p>Certain parts of the room are cooler or hotter because of the way the ACs blow. Try different places until you find one you like.</p>
<p>You’ll note that the Hitari fan is to help out the new air circulation system; without that extra help, the CO2 levels get past 1500 ppm with full classes.<br />
</p>
<p>&nbsp;</p>
<p><strong>Chatting away etc.</strong></p>
<p>Go ahead and chat away occasionally during work times, but then come back to the task at hand. Think of how work happens at the GooglePlex etc. now-a-days.</p>
<p>&nbsp;</p>
<p><strong>Class lectures etc.</strong></p>
<p>They can be more interactive and free-ranging, not necessarily following all the details of the notes **as long as** you are going to follow up reading all of the notes, and know that you are responsible for everything there, whether covered in class or not. And this seems to work for you at this school.<br />
</p>
<p>&nbsp;</p>
<p><strong>Notebook</strong></p>
<p>To be able to put things the way that *your* brain will be able to recall, but connecting to what’s there already.<br />
  A notebook grade, as a bonus mark, is actually not a bad idea.</p>
<p>&nbsp;</p>
<p><strong>Test-taking routines</strong></p>
<p>Issue of putting answers all over the place. Better when you want to leave a question for later to leave a space and come back. If continuing a question some place else, like at the end of the test, **clearly** indicate this. But preferable is to leave a space and come back.</p>
<p> •	Leave lines between your questions.<br />
  •	Point form and diagram assistance are good.<br />
  •	Neat!!!<br />
  •	5 minutes reading/peeing time (mainly for last minute questions/clarifications<br />
  •	Scratch out / cross out (preferably) neatly.</p>
<p>Note that tests and their questions will be harder than the homework assignments/questions.  And even if there are similar questions, they will be graded a lot more critically on tests than on homework assignments.</p>
<p>&nbsp;</p>
<p><strong>HL Only times, and SL options to stay</strong></p>
<p>HL topics are HL students only, certainly HW/tests and grading. So there will be a lot of time where SL will be off on their own in the Library etc.</p>
<p>&nbsp;</p>
<p><strong>Miscellaneous points</strong></p>
<p>If I make a mistake in one of the questions of the homework, or I make a mistake with the posting of the homework, please let me know. Likewise, please let me know if/when a particular homework assignment is too much/too hard.<br />
